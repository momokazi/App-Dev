/*Suppose, your distance to office from home is 25 km and you travel 40 km per
hour. Write a program to calculate time taken to reach office in
minutes. Formula= (distance) / (speed). */

void main() {
  double dis = 25.0;

  double trav = 40.0;

  double time = (dis / trav) * 60;

  print(time);
}
